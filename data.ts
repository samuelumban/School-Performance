export const getInitialSchools = (): School[] => {
  return rawData.split('\n').map((line, index) => {
    const parts = line.split('\t');
    // Basic cleaning, some rows might have different spacing
    const npsn = parts[0]?.trim() || `MISSING-${index}`;
    const name = parts[1]?.trim() || 'Unknown School';
    const status = parts[2]?.trim() || '';
    const province = parts[3]?.trim() || '';
    
    let type: 'SMAK' | 'SMTK' = 'SMAK';
    if (name.includes('SMTK')) type = 'SMTK';

    return {
      id: npsn, // Use NPSN as ID
      npsn,
      name,
      type,
      status,
      province,
      totalScore: 0,
      eventsParticipated: 0,
      totalEventsPossible: 0,
      participatedEventIds: []
    };
  }).filter(s => s.name !== 'Unknown School' && s.name !== '');
};